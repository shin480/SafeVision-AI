import cv2


# ===============================
# 1. 기본 카메라 연결
# ===============================
# 0번 카메라: 기본 웹캠 또는 첫 번째 카메라
cap = cv2.VideoCapture(0)


# ===============================
# 2. 카메라 연결 확인
# ===============================
if not cap.isOpened():
    print("카메라를 열 수 없습니다.")
    exit()


print("카메라 1개 테스트 시작")
print("q 키를 누르면 종료됩니다.")


# ===============================
# 3. 카메라 화면 출력
# ===============================
while True:
    # 카메라에서 한 프레임 읽기
    ret, frame = cap.read()

    # 프레임을 못 읽으면 종료
    if not ret:
        print("프레임을 읽을 수 없습니다.")
        break

    # 화면 출력
    cv2.imshow("Camera Test", frame)

    # q 키를 누르면 종료
    if cv2.waitKey(1) & 0xFF == ord("q"):
        break


# ===============================
# 4. 종료 처리
# ===============================
cap.release()
cv2.destroyAllWindows()